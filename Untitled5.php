<h1 align="center"><font color="gray"><font size="30">ترتكزفكرة التمويل الأصغر علي تقديم قروض ميسرة وبأسعار فائدة تجارية لتنمية المجتمعات الفقيرة اقتصادياً واجتماعياً،ولتخفيف حدة الفقر  وتقليل الفجوة الطبقية بين شرائح المجتمع 
<h1 align="center"><font color="gray"><font size="30"> اشكال التمويل
تمويل مباشر :-
هذا النوع من التمويل يعبر عن العلاقة المباشرة بين المقروض والمقترض والمستثمر دون تدخل اى وسيط مالى مصرفى او غير مصرفي وهذا النوع من انواع التمويل يتخذ صورا متعددة كما يختلف بأختلاف المقترضين .
تمويل غير مباشر :-
يعتبر هذا النوع عن كل طرق واساليب التمويل غير المباشرة وهذا يعنى ان كل المصادر المالية فيها وسطاء ماليين حيث يقوم الوسيط المالي المتمثل فى الاسواق المالية وبعض البنوك بتجميع المدخرات المالية من الوحدات الاقتصادية ذات الفائض ثم توزيع هذه الادخارات المالية على الوحدات الاقتصادية التى تحتاجها .
التمويل المحلي :-
ينقسم هذا النوع من التمويل الى تمويل السوق والمؤسسات المالية والاسواق المالية المحلية وهو يضم المصادر المباشرة والغير مباشرة بمختلف انواعها وهذا النوع من التمويل يخدم قطاع المؤسسات الاقتصادية اكثر من الهيئات الحكومية .
التمويل الدولي :-
هذا النوع يعتمد بالدرجة الاولى على الاسواق المالية مثل البورصات والهيئات المالية والدولية او الاقليمية مثل صندوق النقد الدولي او البنك العالم للأنشاء والتعمير وبعض المؤسسات الأقليمية بالأضافة الى البرامج التمويلية من الدولة التى تأتى فى شكل أعانات واستثمارات .
